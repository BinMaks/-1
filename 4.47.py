a = float(input("Введите a: "))
b = float(input("Введите b: "))
c = float(input("Введите c: "))
if a < b < c:
    print("Неравенство a < b < c выполняется")
else:
    print("Неравенство a < b < c не выполняются")

if b > a > c:
    print("Неравенство b > a > c выполняется")
else:
    print("Неравенство b > a > c не выполняется ")