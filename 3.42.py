a2 = int(input("Введите a2 (десятки двузначного числа): "))
a1 = int(input("Введите a1 (единицы двузначного числа): "))
b1 = int(input("Введите b1 (цифра однозначного числа): "))
if a2 < 1 or a2 > 9:
    print("Ошибка: a2 должно быть от 1 до 9.")
elif a1 < 0 or a1 > 9:
    print("Ошибка: a1 должно быть от 0 до 9.")
elif b1 < 0 or b1 > 9:
    print("Ошибка: b1 должно быть от 0 до 9.")
else:
    n = 100 * a2 + 10 * a1 + b1
    print(f"Полученное трехзначное число: {n}")