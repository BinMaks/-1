x1_1, y1_1 = map(float, input("Координаты левого нижнего угла 1-го прямоугольника (x y): ").split())
x2_1, y2_1 = map(float, input("Координаты правого нижнего угла 1-го прямоугольника (x y): ").split())
x1_2, y1_2 = map(float, input("Координаты левого нижнего угла 2-го прямоугольника (x y): ").split())
x2_2, y2_2 = map(float, input("Координаты правого нижнего угла 2-го прямоугольника (x y): ").split())
min_x = min(x1_1, x1_2)
min_y = min(y1_1, y1_2)
max_x = max(x2_1, x2_2)
max_y = max(y2_1, y2_2)
print(f"Левый нижний угол: ({min_x}, {min_y}) ")
print(f"Правый верхний угол: ({max_x}, {max_y}) ")
