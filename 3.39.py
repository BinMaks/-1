x = int(input("Введите трехзначное число: "))
if x < 100 or x > 999:
    print("Ошибка: число должно быть от 100 до 999.")
else:
    a = x // 100
    b = (x // 10) % 10
    c = x % 10
    m = 10 * a + c
    n = m * 10 + b
    print(f"Исходное число: {x}")
    print(f"Цифры: a = {a}, b = {b}, c = {c}")
    print(f"После зачеркивания второй цифры: {m}")
    print(f" Приписали {b} справа: {n}")
