print("Введите цифры трехзначного числа: ")
a3 = int(input("a3 (сотни): "))
a2 = int(input("a2 (десятки): "))
a1 = int(input("a1 (единицы): "))
print("Введите цифры двузначного числа: ")
b2 = int(input("b2 (десятки): "))
b1 = int(input("b1 (единицы): "))
if not (1 <= a3 <= 9):
    print("Ошибка: a3 должно быть от 1 до 9.")
elif not (0 <= a2 <= 9) or not (0 <= a1 <= 9):
    print("Ошибка: a2 и a1 должны быть от 0 до 9.")
elif not (1 <= b2 <= 9) or not (0 <= b1 <= 9):
    print("Ошибка: b2 от 1 до 9, b1 от 0 до 9.")
else:
    A = 100 * a3 + 10 * a2 + a1
    B = 10 * b2 + b1
    S = A + B
    if S < 100 or S > 999:
        print("Ошибка: сумма {S} не является трехзначным числом.")
    else:
        s3 = S // 100
        s2 = (S // 10) % 10
        s1 = S % 10
        print(f"Сумма: {S}")
        print(f"Цифры суммы: s3 = {s3}, s2 = {s2}, s1 = {s1}")
