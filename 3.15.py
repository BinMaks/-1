n = int(input("Введите номер товара (от 1 до 50):"))
if n < 1 or n > 50:
    print("Ошибка: всего 50 мест на складе.")
else:
    товаров_в_ряду = 10
    ряд = (n - 1) // товаров_в_ряду + 1
    ярус = (n - 1) % товаров_в_ряду + 1
    номер_на_ярусе = ряд
    print(f" Товар {n}:")
    print(f" Ряд: {ряд}:")
    print(f" Ярус: {ярус}:")
    print(f" Номер на ярусе: {номер_на_ярусе}:")


