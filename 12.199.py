def number_to_words(n):
    if n == 0:
        return "ноль"
    ones = ["", "один", "два", "три", "четыре", "пять", "шесть", "семь", "восемь", "девять",
            "десять", "одиннадцать", "двенадцать", "тринадцать", "четырнадцать", "пятнадцать",
            "шестнадцать", "семнадцать", "восемнадцать", "девятнадцать"]
    tens = ["", "", "двадцать", "тридцать", "сорок", "пятьдесят", "шестьдесят", "семьдесят",
            "восемьдесят", "девяносто"]
    hundreds = ["", "сто", "двести", "триста", "четыреста", "пятьсот", "шестьсот", "семьсот",
               "восемьсот", "девятьсот"]
    if n < 20:
        return ones[n]
    elif n < 100:
        return tens[n // 10] + ('' if n % 10 == 0 else ' ' + ones[n % 10])
    elif n < 1000:
        return hundreds[n // 100] + ('' if n % 100 == 0 else ' ' + number_to_words(n % 100))
    else:
        return "тысяча" + ('' if n % 1000 == 0 else ' ' + number_to_words(n % 1000))
n = int(input("Введите число (≤ 1000): "))
print(number_to_words(n))