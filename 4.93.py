k = int(input("Введите k (1 <= k <= 365): "))
day_of_week = (k % 7)
days = ["Понедельник" , "вторник" , "среда" , "четверг" , "пятница" , "суббота" , "воскресенье"]
print(f"k-й день года - {days[day_of_week]}")

k, d = map(int, input("Введите k и d (1 <= k <= 365, 1 <= d <= 7): ").split())
day_of_week = (k + d - 2) % 7
days = ["Понедельник" , "вторник" , "среда" , "четверг" , "пятница" , "суббота" , "воскресенье"]
print(f"k-й день года - {days[day_of_week]}")