n = int(input("Введите трехзначное число (все цифры различны): "))
if n < 100 or n > 999:
    print("Ошибка: число должно быть трехзначным.")
else:
    a = n // 100
    b = (n // 10) % 10
    c = n % 10
    if len(set([a, b, c])) != 3:
        print("Ошибка: все цифры должны быть различны.")
    else:
        перестановки = [
            a * 100 + b * 10 + c,
            a * 100 + c * 10 + b,
            b * 100 + a * 10 + c,
            b * 100 + c * 10 + a,
            c * 100 + a * 10 + b,
            c * 100 + b * 10 + a
        ]
        print("Шесть чисел, образованных перестановкой цифр:")
        for число in перестановки:
            print(число)
