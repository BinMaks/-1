x = int(input("Введите трехзначное число: "))
if x < 100 or x > 999:
    print("Ошибка: число должно быть от 100 до 999.")
else:
    a = x // 100
    b = (x // 10) % 10
    c = x % 10
    m = 10 * a + b
    m_rev = 10 * b + a
    n = m_rev * 10 + c
    print(f"Исходное число: {x}")
    print(f"Цифры: a = {a}, b = {b}, с = {c}")
    print(f"После зачеркивания: {m}")
    print(f"После перестановки: {m_rev}")
    print(f"Результат (n): {n}")