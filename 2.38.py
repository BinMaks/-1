a = int(input("Введите первое число: "))
b = int(input("Введите второе число: "))
summa = a + b
raznost = a - b
proizvedenie = a * b
chastnoe = a/b
print(f"\n ---- Результаты ----")
print(f"{a} + {b} = {summa}")
print(f"{a} - {b} = {raznost}")
print(f"{a} * {b} = {proizvedenie}")
print(f"{a} / {b} = {chastnoe: .2f}")

