num = int(input("введите четырехзначное число: "))
if '2' in str(num) or '7' in str(num):
    print("В числе есть 2 или 7")
else:
    print("В числе нет 2 и 7")
if '3' in str(num) or '6' in str(num) or '9' in str(num):
    print("В числе есть 3, 6 или 9")
else:
    print("В числе нет 3, 6 и 9 ")