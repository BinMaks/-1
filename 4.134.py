month_num = int(input("Введите номер месяца (1–12): "))
months = ["январь", "февраль", "март", "апрель", "май", "июнь",
          "июль", "август", "сентябрь", "октябрь", "ноябрь", "декабрь"]
if 1 <= month_num <= 12:
    print(f"Это {months[month_num - 1]}")
else:
    print("Некорректный номер месяца")