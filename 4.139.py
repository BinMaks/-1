card_num = int(input("Введите номер карты: "))
card_values = {
    6: "шестёрка", 7: "семёрка", 8: "восьмёрка", 9: "девятка", 10: "десятка",
    11: "валет", 12: "дама", 13: "король", 17: "туз"
}
if card_num in card_values:
    print(f"Достоинство карты: {card_values[card_num]}")
else:
    print("Некорректный номер карты")